    private class AktualTableModel extends AbstractTableModel {
        
        private List<Data> listDataAktual;
        private String[] header = {"Tanggal","Rata2 Suhu","Jumlah Cacat Produk"};
        
        public AktualTableModel(List<Data> listDatas) {
            this.listDataAktual = listDatas;
        }

        @Override
        public int getRowCount() {
            return listDataAktual.size();
        }

        @Override
        public int getColumnCount() {
            return header.length;
        }

        @Override
        public String getColumnName(int column) {
            return header[column];
        }
        
        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Data d = listDataAktual.get(rowIndex);
            switch(columnIndex){
                case 0:return d.getTanggal();
                case 1:return d.getSuhuRuangan();
                case 2:return d.getJumlahCacat();
                default:return "";
            }
        }
        
    }
    
